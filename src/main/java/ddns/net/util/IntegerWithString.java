package ddns.net.util;

public class IntegerWithString {
    private int i;
    private String s;

    public IntegerWithString(int i,String s){
        this.i = i;
        this.s = s;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}
